        { return p1+p2; }

        public static int Subract(int p1, int p2)
        { return p1-p2; }

        public static int Multiply(int p1, int p2)
        { return p1 * p2; }

        public static int Divide(int p1, int p2)
        { return p1 / p2; }
    }
